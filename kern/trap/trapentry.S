.code64

.globl _trap_entry
_trap_entry:
    # Save all general-purpose registers (must match TrapRegisters layout)
    pushq %rax
    pushq %rcx
    pushq %rdx
    pushq %rbx
    pushq %rbp
    pushq %rsi
    pushq %rdi
    pushq %r8
    pushq %r9
    pushq %r10
    pushq %r11
    pushq %r12
    pushq %r13
    pushq %r14
    pushq %r15

    # First argument (rdi) = pointer to TrapFrame = current rsp
    movq %rsp, %rdi

    call trap

    # Restore all general-purpose registers
    popq %r15
    popq %r14
    popq %r13
    popq %r12
    popq %r11
    popq %r10
    popq %r9
    popq %r8
    popq %rdi
    popq %rsi
    popq %rbp
    popq %rbx
    popq %rdx
    popq %rcx
    popq %rax

    addq $16, %rsp    # skip trapno and error code
    iretq

.section .note.GNU-stack,"",@progbits